// rotina para definição da intensidade de cores do led
uint32_t matrix_rgb(double b, double r, double g)
{
    unsigned char R, G, B;
    R = r * 255;
    G = g * 255;
    B = b * 255;
    return (G << 24) | (R << 16) | (B << 8);
}

// Função para acionar a matriz de LEDs - ws2812b
void desenho_pio(double frame[FRAME_SIZE][3], uint32_t valor_led, PIO pio, uint sm)
{
    for (int i = 0; i < FRAME_SIZE; i++)
    {
        double r = frame[i][0];
        double g = frame[i][1];
        double b = frame[i][2];
        valor_led = matrix_rgb(r, g, b);
        pio_sm_put_blocking(pio, sm, valor_led);
    }
}

void exibir_numero(PIO pio, uint sm)
{
    uint32_t current_time = to_ms_since_boot(get_absolute_time());
    if (current_time - last_time_matrix >= interval_matrix)
    {
        desenho_pio(frames_numeros[contador], 0, pio, sm);
        last_time_matrix = current_time;
    }
}

// rotina da interrupção dos botoes
static void gpio_irq_handler(uint gpio, uint32_t events)
{

    uint32_t current_time = to_ms_since_boot(get_absolute_time());
    if (current_time - last_time_button >= interval_button)
    {
        if (gpio == button_A)
        {
            green_led_state = !green_led_state;

            if (green_led_state)
                strcpy(msgVerde, "Verde ON");
            else
                strcpy(msgVerde, "Verde OFF");
            printf("Botao A pressionado");
        }

        if (gpio == button_B)
        {
            blue_led_state = !blue_led_state;

            if (blue_led_state)
                strcpy(msgAzul, "Azul ON");
            else
                strcpy(msgAzul, "Azul OFF");

            printf("Botao B pressionado");
        }

        set_led_color(red_led_state, green_led_state, blue_led_state);
        last_time_button = current_time;
    }
}

// função principal
int main()
{

    stdio_init_all(); // Inicializa comunicação USB CDC para monitor serial

    // I2C Initialisation. Using it at 400Khz.
    i2c_init(I2C_PORT, 400 * 1000);

    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);                    // Set the GPIO pin function to I2C
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);                    // Set the GPIO pin function to I2C
    gpio_pull_up(I2C_SDA);                                        // Pull up the data line
    gpio_pull_up(I2C_SCL);                                        // Pull up the clock line
    ssd1306_t ssd;                                                // Inicializa a estrutura do display
    ssd1306_init(&ssd, WIDTH, HEIGHT, false, endereco, I2C_PORT); // Inicializa o display
    ssd1306_config(&ssd);                                         // Configura o display
    ssd1306_send_data(&ssd);                                      // Envia os dados para o display

    // Limpa o display. O display inicia com todos os pixels apagados.
    ssd1306_fill(&ssd, false);
    ssd1306_send_data(&ssd);

    bool cor = true;
    char c;

    setup_gpio(RED_PIN);
    setup_gpio(GREEN_PIN);
    setup_gpio(BLUE_PIN);

    PIO pio = pio0;
    bool ok;
    uint16_t i;
    uint32_t valor_led;
    double r = 0.0, b = 0.0, g = 0.0;

    // coloca a frequência de clock para 128 MHz, facilitando a divisão pelo clock
    ok = set_sys_clock_khz(128000, false);

    // Inicializa todos os códigos stdio padrão que estão ligados ao binário.
    stdio_init_all();

    printf("iniciando a transmissão PIO");
    if (ok)
        printf("clock set to %ld\n", clock_get_hz(clk_sys));

    // configurações da PIO
    uint offset = pio_add_program(pio, &pio_matrix_program);
    uint sm = pio_claim_unused_sm(pio, true);
    pio_matrix_program_init(pio, sm, offset, OUT_PIN);

    // inicializar o botão de interrupção - GPIO5
    gpio_init(button_A);
    gpio_set_dir(button_A, GPIO_IN);
    gpio_pull_up(button_A);

    // inicializar o botão de interrupção - GPIO5
    gpio_init(button_B);
    gpio_set_dir(button_B, GPIO_IN);
    gpio_pull_up(button_B);

    // interrupção da gpio habilitada
    gpio_set_irq_enabled_with_callback(button_A, GPIO_IRQ_EDGE_FALL, 1, &gpio_irq_handler);
    gpio_set_irq_enabled_with_callback(button_B, GPIO_IRQ_EDGE_FALL, 1, &gpio_irq_handler);

    while (true)
    {
        uint32_t current_time = to_ms_since_boot(get_absolute_time());

        // Verifica se o intervalo passou e atualiza LCD
        if (current_time - last_time_lcd >= interval_lcd)
        {
            cor = !cor;
            // Atualiza o conteúdo do display com animações
            ssd1306_fill(&ssd, !cor);                     // Limpa o display
            ssd1306_rect(&ssd, 3, 3, 122, 58, cor, !cor); // Desenha um retângulo
            ssd1306_draw_string(&ssd, msgVerde, 16, 8);   // Desenha uma string
            ssd1306_draw_string(&ssd, msgAzul, 16, 28);   // Desenha uma string
            ssd1306_draw_string(&ssd, caractere, 16, 48); // Desenha uma string
            ssd1306_send_data(&ssd);                      // Atualiza o display
            last_time_lcd=current_time;
        }

        if (stdio_usb_connected())
        { // Certifica-se de que o USB está conectado
            if (scanf("%c", &c) == 1)
            { // Lê caractere da entrada padrão
                caractere[10] = c;  // Adiciona na string do LCD
                printf("Recebido: '%c'\n", c);
            }
        }

        if (c >= '0' && c <= '9')
        {
            contador = c - '0'; // Transforma de char para int
            exibir_numero(pio, sm); // Imprime na matriz de leds
        }
    }
}
